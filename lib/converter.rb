class RomanNumerals

  def converter(number)
    if number == 1
      "I"
    elsif number == 2
      "II"
    elsif number == 3
      "III"
    elsif number == 4
      "IV"
    elsif number == 5
      "V"
    elsif number == 6
      "VI"
    elsif number == 7
      "VII"
    elsif number == 8
      "VIII"
    elsif number == 9
      "IX"
    else number == 10
      "X"
    end
  end
end
